package MudaMuda;

public class Muda {
    private double d;
    private double r;

    public Muda(double d, double r) {
        this.d = d;
        this.r = r;
    }

    public double getD() {
        return d;
    }

    public void setD(double d) {
        this.d = d;
    }

    public double getR() {
        return r;
    }

    public void setR(double r) {
        this.r = r;
    }
}
